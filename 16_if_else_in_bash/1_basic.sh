# Decision Making is what If Else Conditions comes in play :)

# Syntax for If-Else Statement :
################################
#                              #
#       if [ CONDITION ]       #
#       then                   #
#         command...           #
#       else                   #
#         command...           #
#       fi                     #
#                              #
################################

##############################
###--->>> CONDITIONS <<<---###
##############################
# [ == ] OR [ -eq ] #Equal To
# [ != ] OR [ -ne ] # Not Equal To
# [-lt]             # Less Than
# [ -gt ]           # Greater Than
# [-ge]             # Grater Than Equal's To
# [ -le ]           # Less than Equal's To
# [[ =~ ]]          # Regex Matching
# [ !expr ]         # Not
# [ x && y ]        # AND
# [ x||y ]          # OR
